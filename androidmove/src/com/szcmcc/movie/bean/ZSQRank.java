package com.szcmcc.movie.bean;

import java.io.Serializable;

public class ZSQRank implements Serializable {
	/**
	 * 
	 */
	private static final long serialVersionUID = 26196053225829804L;
	private String rankNo;
	private String movieName;
	private String rankCount;
	private String rankDate;
	private String movieId;

	public String getRankNo() {
		return rankNo;
	}

	public void setRankNo(String rankNo) {
		this.rankNo = rankNo;
	}

	public String getMovieName() {
		return movieName;
	}

	public void setMovieName(String movieName) {
		this.movieName = movieName;
	}

	public String getRankCount() {
		return rankCount;
	}

	public void setRankCount(String rankCount) {
		this.rankCount = rankCount;
	}

	public String getRankDate() {
		return rankDate;
	}

	public void setRankDate(String rankDate) {
		this.rankDate = rankDate;
	}

	public String getMovieId() {
		return movieId;
	}

	public void setMovieId(String movieId) {
		this.movieId = movieId;
	}

}
