package com.szcmcc.movie.bean;

import java.util.ArrayList;

public class MovieTicketList {

	public ArrayList<MovieTicket> mMovieTickets = new ArrayList<MovieTicket>();

	public void release() {
		mMovieTickets.clear();
	}

	public void addMovieTicket(MovieTicket mMovieTicket) {
		mMovieTickets.add(mMovieTicket);
	}
}
