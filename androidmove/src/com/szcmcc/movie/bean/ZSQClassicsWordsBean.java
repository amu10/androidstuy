package com.szcmcc.movie.bean;

import java.io.Serializable;

public class ZSQClassicsWordsBean implements Serializable{
	/**
	 * 
	 */
	private static final long serialVersionUID = 3516984238828325237L;
	private int wordId;
	private String content;
	private String sourceMovie;
	private String imgAddr;

	public int getWordId() {
		return wordId;
	}

	public void setWordId(int wordId) {
		this.wordId = wordId;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getSourceMovie() {
		return sourceMovie;
	}

	public void setSourceMovie(String sourceMovie) {
		this.sourceMovie = sourceMovie;
	}

	public String getImgAddr() {
		return imgAddr;
	}

	public void setImgAddr(String imgAddr) {
		this.imgAddr = imgAddr;
	}

}
