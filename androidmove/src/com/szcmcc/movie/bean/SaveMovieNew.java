package com.szcmcc.movie.bean;

public class SaveMovieNew {

	public String m_id;
	public String name ;
	public String imageUrl ;
	public String play_status ;
	public String playtime ;

	public SaveMovieNew(String id,String name,String imageUrl,String play_status,String playtime) {
		super();
		this.m_id = id;
		this.name = name;
		this.imageUrl = imageUrl;
		this.play_status = play_status;
		this.playtime = playtime;

	}
	

	public String getId() {
		return m_id;
	}


	public void setId(String id) {
		this.m_id = id;
	}


	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getImageUrl() {
		return imageUrl;
	}

	public void setImageUrl(String imageUrl) {
		this.imageUrl = imageUrl;
	}

	public String getPlay_status() {
		return play_status;
	}

	public void setPlay_status(String play_status) {
		this.play_status = play_status;
	}

	public String getPlaytime() {
		return playtime;
	}

	public void setPlaytime(String playtime) {
		this.playtime = playtime;
	}
	
	
}
